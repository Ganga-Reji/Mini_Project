<script>
// Replace 'YOUR_API_KEY' with your actual RAWG API key
const apiKey = 'fc11aa78d0bb490996b18d17ec68ca7e';

// Constants for pagination
const gamesPerPage = 8;
let currentPage = 1;

// Function to fetch top 250 games and update the cards
function fetchTopGames(page) {
  fetch(`https://api.rawg.io/api/games?key=${apiKey}&ordering=-rating,-popularity&page_size=${gamesPerPage}&page=${page}`)
    .then(response => response.json())
    .then(data => {
      const gameCards = document.getElementById('game-card');
      gameCards.innerHTML = '';

      data.results.forEach(game => {
        const cardDiv = document.createElement('div');
        cardDiv.className = 'col-md-3 mb-4';
        cardDiv.innerHTML = `
          <a href="game-details.php?id=${game.id}" class="game-link">
            <div class="card game-card">
              <img src="${game.background_image}" class="card-img-top img-thumbnail" alt="${game.name} Poster">
              <div class="card-body">
                <h6 class="card-text">${game.name}</h6>
              </div>
            </div>
          </a>
        `;
        gameCards.appendChild(cardDiv);
      });

      // Update pagination
      updatePagination(data.count);
    })
    .catch(error => {
      console.error('Error:', error);
    });
}

// Function to update pagination
function updatePagination(totalGames) {
  const pagination = document.getElementById('pagination');
  pagination.innerHTML = '';

  const totalPages = Math.ceil(totalGames / gamesPerPage);

  // Add "Previous" button
  if (currentPage > 1) {
    const prevLi = document.createElement('li');
    prevLi.className = 'page-item';
    prevLi.innerHTML = `<a class="page-link" href="#" onclick="changePage(${currentPage - 1})">Previous</a>`;
    pagination.appendChild(prevLi);
  }

  // Generate page buttons
  for (let i = Math.max(1, currentPage - 3); i <= Math.min(totalPages, currentPage + 4); i++) {
    const li = document.createElement('li');
    li.className = `page-item ${i === currentPage ? 'active' : ''}`;
    li.innerHTML = `<a class="page-link" href="#" onclick="changePage(${i})">${i}</a>`;
    pagination.appendChild(li);
  }

  // Add "Next" button
  if (currentPage < totalPages) {
    const nextLi = document.createElement('li');
    nextLi.className = 'page-item';
    nextLi.innerHTML = `<a class="page-link" href="#" onclick="changePage(${currentPage + 1})">Next</a>`;
    pagination.appendChild(nextLi);
  }
}

// Function to change the current page
function changePage(newPage) {
  if (newPage === currentPage) return;
  currentPage = newPage;
  fetchTopGames(newPage);
}

// Initial load
fetchTopGames(currentPage);
</script>
